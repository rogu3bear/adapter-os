//!
//! This module provides HTTP handlers for dataset management operations including:
//! - Upload and creation of training datasets
//! - CRUD operations (list, get, delete)
//! - Dataset versioning
//! - File management within datasets
//! - Validation and safety checks
//! - Chunked upload for large files
//! - Progress tracking via SSE
//! - Creation from existing documents

