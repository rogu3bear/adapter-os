#!/usr/bin/env bash
set -euo pipefail
IFS=$'\n\t'

usage() {
  cat <<'USAGE'
Build a deterministic codebase dataset for adapter training.

Outputs:
  var/datasets/codebase/
    manifest.jsonl
    chunks/<chunk_id>.txt

Usage:
  scripts/build-codebase-dataset.sh [--out <path>] [--no-clean]

Options:
  --out <path>   Output directory (default: var/datasets/codebase)
  --no-clean     Do not delete existing output dir before writing
  -h, --help     Show this help
USAGE
}

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"

DATASETS_ROOT="${REPO_ROOT}/var/datasets"
OUT_DIR="${CODEBASE_DATASET_OUT:-"${DATASETS_ROOT}/codebase"}"
CLEAN_OUT=1

while [[ $# -gt 0 ]]; do
  case "$1" in
    --out)
      OUT_DIR="$2"
      shift 2
      ;;
    --no-clean)
      CLEAN_OUT=0
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "Unknown argument: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

if [[ "${OUT_DIR}" != /* ]]; then
  OUT_DIR="${REPO_ROOT}/${OUT_DIR}"
fi
OUT_DIR="${OUT_DIR%/}"

if [[ "${OUT_DIR}" == "${REPO_ROOT}" || "${OUT_DIR}" == "${REPO_ROOT}/var" || "${OUT_DIR}" == "${DATASETS_ROOT}" ]]; then
  echo "Refusing to write to unsafe output dir (got: ${OUT_DIR})" >&2
  exit 2
fi

if [[ "${OUT_DIR}" != "${DATASETS_ROOT}"/* ]]; then
  echo "Refusing to write outside ${DATASETS_ROOT} (got: ${OUT_DIR})" >&2
  exit 2
fi

if ! command -v python3 >/dev/null 2>&1; then
  echo "python3 is required but was not found on PATH" >&2
  exit 1
fi

if [[ "${CLEAN_OUT}" -eq 1 ]]; then
  rm -rf "${OUT_DIR}"
fi
mkdir -p "${OUT_DIR}/chunks"

python3 - "${REPO_ROOT}" "${OUT_DIR}" <<'PY'
import hashlib
import json
import sys
from pathlib import Path

REPO_ROOT = Path(sys.argv[1]).resolve()
OUT_DIR = Path(sys.argv[2]).resolve()
CHUNKS_DIR = OUT_DIR / "chunks"
MANIFEST_PATH = OUT_DIR / "manifest.jsonl"

DATASET_ID = "codebase"
SCHEMA_VERSION = 1

CHUNK_MIN_CHARS = 4_000
CHUNK_TARGET_CHARS = 12_000
CHUNK_MAX_CHARS = 16_000

EXCLUDED_DIR_PARTS = {
    ".git",
    ".next",
    ".turbo",
    "build",
    "dist",
    "node_modules",
    "target",
    "__generated__",
    "generated",
}

EXCLUDED_BASENAMES = {
    "Cargo.lock",
    "package-lock.json",
    "pnpm-lock.yaml",
    "yarn.lock",
}

GENERATED_MARKERS_LOWER = (
    "@generated",
    "autogenerated",
    "automatically generated",
    "generated by",
    "do not edit",
    "don't edit",
)

INCLUDE_GLOBS = [
    ("rust", "src/**/*.rs"),
    ("rust", "crates/**/src/**/*.rs"),
    ("rust", "xtask/**/src/**/*.rs"),
    ("rust", "crates/adapteros-ui/src/**/*.rs"),
    ("markdown", "docs/**/*.md"),
]


def sha256_hex(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def is_excluded_path(path: Path) -> bool:
    if path.name in EXCLUDED_BASENAMES:
        return True
    if path.name.endswith(".d.ts"):
        return True
    if path.name.endswith(".min.ts") or path.name.endswith(".min.tsx"):
        return True
    parts = set(path.parts)
    if parts & EXCLUDED_DIR_PARTS:
        return True
    return False


def looks_generated(normalized_text: str) -> bool:
    head = normalized_text[:4096].lower()
    return any(marker in head for marker in GENERATED_MARKERS_LOWER)


def normalize_text(raw_bytes: bytes) -> str:
    text = raw_bytes.decode("utf-8", errors="replace")
    return text.replace("\r\n", "\n").replace("\r", "\n")


def chunk_lines(lines: list[str]):
    i = 0
    n = len(lines)
    while i < n:
        start = i
        chars = 0
        last_blank_cut = None

        while i < n:
            line = lines[i]

            if chars > 0 and chars + len(line) > CHUNK_MAX_CHARS and chars >= CHUNK_MIN_CHARS:
                break

            chars += len(line)
            i += 1

            if chars >= CHUNK_MIN_CHARS and line.strip() == "":
                last_blank_cut = i

            if chars >= CHUNK_TARGET_CHARS and last_blank_cut is not None:
                i = last_blank_cut
                break

            if chars >= CHUNK_MAX_CHARS:
                break

        if i == start:
            i = min(start + 1, n)

        yield start, i


def discover_files():
    seen: dict[str, tuple[Path, str]] = {}
    for language, glob_pattern in INCLUDE_GLOBS:
        for path in REPO_ROOT.glob(glob_pattern):
            if not path.is_file():
                continue
            if is_excluded_path(path):
                continue
            rel = path.relative_to(REPO_ROOT).as_posix()
            if rel in seen:
                continue
            seen[rel] = (path, language)

    return [(rel, seen[rel][0], seen[rel][1]) for rel in sorted(seen.keys())]


def main() -> None:
    entries: list[dict] = []
    file_count = 0
    chunk_count = 0

    files = discover_files()

    for relpath, abspath, language in files:
        raw = abspath.read_bytes()
        normalized = normalize_text(raw)
        if normalized.strip() == "":
            continue
        if looks_generated(normalized):
            continue

        normalized_bytes = normalized.encode("utf-8")
        file_sha = sha256_hex(normalized_bytes)

        lines = normalized.splitlines(keepends=True)
        if not lines:
            continue

        file_count += 1

        for chunk_index, (start_i, end_i) in enumerate(chunk_lines(lines)):
            chunk_text = "".join(lines[start_i:end_i])
            if chunk_text.strip() == "":
                continue

            chunk_bytes = chunk_text.encode("utf-8")
            chunk_sha = sha256_hex(chunk_bytes)
            start_line = start_i + 1
            end_line = end_i

            chunk_id_input = (
                f"{DATASET_ID}-v{SCHEMA_VERSION}\0{relpath}\0{start_line}\0{chunk_sha}".encode(
                    "utf-8"
                )
            )
            chunk_id = sha256_hex(chunk_id_input)

            chunk_relpath = f"chunks/{chunk_id}.txt"
            chunk_abspath = OUT_DIR / chunk_relpath

            chunk_abspath.parent.mkdir(parents=True, exist_ok=True)
            chunk_abspath.write_bytes(chunk_bytes)

            estimated_tokens = max(1, len(chunk_bytes) // 4)

            entries.append(
                {
                    "byte_len": len(chunk_bytes),
                    "chunk_id": chunk_id,
                    "chunk_index": chunk_index,
                    "chunk_sha256": chunk_sha,
                    "chunk_path": chunk_relpath,
                    "dataset_id": DATASET_ID,
                    "end_line": end_line,
                    "estimated_tokens": estimated_tokens,
                    "file_path": relpath,
                    "file_sha256": file_sha,
                    "language": language,
                    "schema_version": SCHEMA_VERSION,
                    "start_line": start_line,
                }
            )
            chunk_count += 1

    MANIFEST_PATH.parent.mkdir(parents=True, exist_ok=True)
    with MANIFEST_PATH.open("w", encoding="utf-8", newline="\n") as f:
        for entry in entries:
            f.write(json.dumps(entry, sort_keys=True, ensure_ascii=False, separators=(",", ":")))
            f.write("\n")

    manifest_sha = sha256_hex(MANIFEST_PATH.read_bytes())

    output_files: list[tuple[str, Path]] = []
    output_files.append(("manifest.jsonl", MANIFEST_PATH))
    for p in sorted(CHUNKS_DIR.glob("*.txt"), key=lambda x: x.name):
        output_files.append((f"chunks/{p.name}", p))

    combined = hashlib.sha256()
    for rel, p in output_files:
        combined.update(rel.encode("utf-8"))
        combined.update(b"\0")
        combined.update(sha256_hex(p.read_bytes()).encode("ascii"))
        combined.update(b"\n")
    dataset_sha = combined.hexdigest()

    print(f"repo_root={REPO_ROOT}")
    print(f"out_dir={OUT_DIR}")
    print(f"files={file_count} chunks={chunk_count}")
    print(f"manifest_sha256={manifest_sha}")
    print(f"dataset_sha256={dataset_sha}")


if __name__ == "__main__":
    main()
PY
